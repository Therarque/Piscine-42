char	*ft_strrev(char *str)
{
	int m;
	int n;
	int r;

	n = 0;
	while (str[n] != 0)
		n++;
	m = -1;
	while (++m < --n)
	{
		r = str[m];
		str[m] = str[n];
		str[n] = r;
	}
	return (str);
}
