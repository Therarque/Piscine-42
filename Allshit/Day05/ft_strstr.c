char	*ft_strstr(char *str, char *to_find)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while ((str[x] != '\0') || (to_find[y] != '\0'))
	{
		if (str[x] == to_find[y])
			return (str + x);
		y++;
		x++;
	}
	return (0);
}
